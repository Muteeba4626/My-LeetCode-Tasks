class Solution {
public:
    int countDigitOne(int n) {
        if (n <= 0) return 0;
        long long count = 0;
        
        // Iterate through each position (1, 10, 100, ...)
        for (long long i = 1; i <= n; i *= 10) {
            long long prefix = n / (i * 10);
            long long current = (n / i) % 10;
            long long suffix = n % i;
            
            if (current == 0) {
                count += prefix * i;
            } else if (current == 1) {
                count += prefix * i + (suffix + 1);
            } else {
                count += (prefix + 1) * i;
            }
        }
        
        return (int)count;
    }
};